package com.datalayer.exam;

public class StudentExamData {
int exam_id;
String exam_name;
int marks;
String examAttempted;
public int getExam_id() {
	return exam_id;
}
public void setExam_id(int exam_id) {
	this.exam_id = exam_id;
}
public String getExam_name() {
	return exam_name;
}
public void setExam_name(String exam_name) {
	this.exam_name = exam_name;
}
public int getMarks() {
	return marks;
}
public void setMarks(int marks) {
	this.marks = marks;
}
public String getExamAttempted() {
	return examAttempted;
}
public void setExamAttempted(String examAttempted) {
	this.examAttempted = examAttempted;
}


}
