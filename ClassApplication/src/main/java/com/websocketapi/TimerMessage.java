package com.websocketapi;

public class TimerMessage {
	String timer;
	boolean examEnded;
	public String getTimer() {
		return timer;
	}
	public void setTimer(String timer) {
		this.timer = timer;
	}
	public boolean isExamEnded() {
		return examEnded;
	}
	public void setExamEnded(boolean examEnded) {
		this.examEnded = examEnded;
	}
	
	
	
}
